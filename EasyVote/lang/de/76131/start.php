    <h1>Wahl des Gemeinderats der Stadt Karlsruhe am 26. Mai 2019</h1>
    <h2>Kumulieren und Panaschieren einfach gemacht - Simulation der Stimmabgabe mit EasyVote</h2>
    <br>
    <p>
        Diese Online-Wahlsimulation soll Ihnen dabei helfen, sich auf die Wahl des Gemeinderats der Stadt Karlsruhe
        vorzubereiten. Dazu bietet Ihnen der Dienst analog zum Papierstimmzettel die Möglichkeit, eine ganze Liste oder
        einzelne Kandidierende auszuwählen. Sie erhalten jeweils eine Rückmeldung, ob Sie bereits alle 48 Stimmen
        vergeben haben sowie einen Vorschlag, wie Sie die im Simulator getroffene Auswahl auf Ihrem Stimmzettel bzw.
        Ihren Stimmzetteln abbilden können. So ist einfacher zu erkennen, wie die eigene Stimme gültig abgegeben
        werden kann.
    </p>
    <p>
        Diese Online-Wahlsimulation basiert auf dem EasyVote Konzept, welches in dem von der Deutschen
        Forschungsgemeinschaft (DFG) geförderten Forschungsprojekt "Verfassungskonforme Umsetzung von elektronischen
        Wahlen" (VerKonWa) von der Forschungsgruppe <a href="https://secuso.aifb.kit.edu/" target="_blank">SECUSO</a>
        entwickelt wurde und die Grundlage der Dissertation von
        <a href="https://de.linkedin.com/in/dr-jurlind-budurushi-247a5b73" target="_blank" style="color: blue;">
            Dr. Jurlind Budurushi</a> an der <a href="http://tuprints.ulb.tu-darmstadt.de/5418/" target="_blank"
                                                style="color: blue;">TU Darmstadt</a> darstellt. Bei der
        Konzept-Erstellung standen die
        Kommunalwahlen
        in Hessen im Fokus und Ziel war es, ein hybrides (elektronisch/Papier) Wahlsystem vorzuschlagen und zu
        evaluieren. Für die Kommunalwahlen 2016 in Hessen wurde diese Online-Wahlsimulation aus dem EasyVote Konzept
        abgeleitet, um Wähler beim Kumulieren und Panaschieren zu unterstützen und ihnen aufzuzeigen, welche Kreuze auf
        dem Stimmzettel welche Auswirkungen haben. Die Regeln für die Kommunalwahlen in Hessen unterscheiden sich
        allerdings von denen in Baden-Württemberg. Die Anpassung der Regeln und die damit verbundenen Änderungen hat
        <a href="https://www.felix-seifert.com/" target="_blank" style="color: blue;">Felix Seifert</a>
        (Wirtschaftsingenieursstudent am <a href="https://secuso.aifb.kit.edu/" target="_blank"
                                            style="color: blue;">KIT</a>) durchgeführt. Beim Hosting der Applikation
        unterstützt
        <a href="https://www.linkedin.com/in/jonas-pfrang-05684012a/" target="_blank" style="color: blue;">Jonas
            Pfrang</a>
        (Wirtschaftsingenieursstudent am <a href="https://secuso.aifb.kit.edu/" target="_blank"
                                            style="color: blue;">KIT</a>) aktiv.
    </p>
    <p>
        Technische Hinweise:
        <ul>
            <li>
                Bitte nutzen Sie nicht die Vorwärts- und Rückwärtsschaltflächen Ihres Webbrowsers, sondern
                ausschließlich die Navigation der Applikation.
            </li>
            <li>
                Die Nutzung dieser Applikation ist zurzeit nicht für Mobilgeräte geeignet.
            </li>
            <li>
                Ihre Auswahl an Kandidaten wird serverseitig nicht gespeichert.
            </li>
            <li>
                Die Online-Wahlsimulation ist neben den vielen Erklärungen von offizieller Seite und Videos ein
                weiterer Ansatz, die bei den Kommunalwahlen doch eher komplizierteren Wahlregeln zu verdeutlichen. Es
                geht hierbei nicht um eine Stimmabgabe. Entsprechend haben wir nicht die Ressourcen aufgewendet, den
                Server und den Dienst so abzusichern, wie das bei einer Stimmabgabe über das Internet der Fall sein
                müsste. Wir speichern keine Daten mit denen Rückschlüsse auf Ihre Auswahl möglich sind.
            </li>
        </ul>
